package classtest;

public class Square {
    // 직사각형의 넒이, 정사각형의 넒이 구하기
    // 메소드 오버로딩

    // 직사각형
    public double areaRectangle(double w, double h) {
        return w * h;
    }
    // 정사각형
    public double areaRectangle(double w) {
        return w * w;
    }
    
    
    
    
    
    
    
    
}
