package ch1;

public class VarLongEx1 {
    public static void main(String[] args) {
        // L, l 대소문자 둘 다 가능 (Long 타입 뒤엔 l : 생략가능 but 붙이는 편)
        long value = 123456789L;
        System.out.println(value);
        System.out.println("value = " + value);
        System.out.println(value + value);
    }
}
